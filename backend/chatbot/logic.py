def get_career_advice(message):
    # Simple logic, replace with AI/ML model or Dialogflow integration
    if "engineering" in message.lower():
        return "Engineering is a great field! You can explore Computer Science, Mechanical, Civil, etc."
    elif "medical" in message.lower():
        return "Medical stream offers MBBS, BDS, Nursing, and more."
    else:
        return "Tell me your interests or subjects you like, and I can suggest career paths."